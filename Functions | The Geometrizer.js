function calCircumference(Radius){
  const Circumference = Math.floor(2 * 3.142 * Radius)
  console.log(`The circumference is ${Circumference} meters`)
}
calCircumference(45);


function calArea(Radius){
  const Area = Math.floor(3.142 * Radius * Radius);
  console.log(`The area is ${Area} meter square`)
}
calArea(15);
